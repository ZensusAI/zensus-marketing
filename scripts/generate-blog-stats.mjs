// Derive per-post stats (word count, in-article image URLs) from the blog
// MDX sources and emit them as a generated TypeScript module. BlogPost.tsx
// folds these into BlogPosting JSON-LD (wordCount + full image array) so
// every future post gets complete schema for free.
//
// Runs at the front of `npm run build`; the output is committed so dev
// servers work without a prior build step.

import { readdir, readFile, writeFile } from "node:fs/promises";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const blogDir = join(__dirname, "..", "src", "content", "blog");
const outPath = join(__dirname, "..", "src", "generated", "blog-stats.ts");

const files = (await readdir(blogDir)).filter((f) => f.endsWith(".mdx")).sort();

const stats = {};
for (const file of files) {
  const slug = file.replace(/\.mdx$/, "");
  const source = await readFile(join(blogDir, file), "utf-8");

  const images = [...source.matchAll(/src="(\/blog\/[^"]+\.(?:webp|png|jpe?g))"/g)].map(
    (m) => `https://zensus.app${m[1]}`,
  );

  const text = source
    .replace(/^import\s.*$/gm, "")
    .replace(/export const meta = \{[\s\S]*?\n\};?/, "")
    .replace(/<[^>]+>/g, " ")
    .replace(/\[([^\]]*)\]\([^)]*\)/g, "$1")
    .replace(/[#*`>]/g, " ");
  const wordCount = text.split(/\s+/).filter(Boolean).length;

  stats[slug] = { wordCount, images };
}

const banner =
  "// Generated by scripts/generate-blog-stats.mjs. Do not edit by hand.\n" +
  "// Regenerate with: node scripts/generate-blog-stats.mjs\n";
const body = `export const blogStats: Record<string, { wordCount: number; images: string[] }> = ${JSON.stringify(stats, null, 2)};\n`;
await writeFile(outPath, banner + body, "utf-8");
console.log(`[blog-stats] wrote ${outPath} (${files.length} posts)`);
